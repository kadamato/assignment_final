public class Author {

    int stt, id;
    String name, email, phone, nationality, description;

    public Author(int stt , int id , String name, String email , String phone , String nationality , String description) {
        this.stt = stt;
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
        this.nationality = nationality;
        this.description = description;
    }
}
